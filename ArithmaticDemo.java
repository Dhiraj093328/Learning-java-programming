class ArithmaticDemo {
    public static void main(String[] args) {

        int no1 = 20;
        int no2 = 35;

        System.out.println("Addition of no1 + no2: " + (no1 + no2));
        System.out.println("Addition of no1 - no2: " + (no1 - no2));
        System.out.println("Addition of no1 * no2: " + (no1 * no2));
        System.out.println("Addition of no1 / no2: " + (no1 / no2));
        System.out.println("Addition of no1 % no2: " + (no1 % no2));


    }
}

        //         OUTPUT

		// Addition of no1 + no2: 55
		// Addition of no1 - no2: -15
		// Addition of no1 * no2: 700
		// Addition of no1 / no2: 0
		// Addition of no1 % no2: 20
