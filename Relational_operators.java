// Relational operators -->(>,<,>=,<==,!=)

class Relational_operators
{

    public static void main(String[] args) {

        int no1 = 20;
        int no2 = 35;

        System.out.println("IS no1 > no2: " + (no1 > no2));
        System.out.println("Is no1 < no2: " + (no1 < no2));
        System.out.println("Is  no1 >= no2: " + (no1 >= no2));
       //System.out.println("IS no1 >== no2: " + (no1 >== no2));
        System.out.println("Is no1  != no2: " + (no1 != no2));

    }

}
