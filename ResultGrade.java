class ResultGrade {

    public static void main(String[] args) {

        int marks = 85;

        if (marks >= 90) {
            System.out.println("Grade A");
        } else if (marks >= 75) {
            System.out.println("Grade B");
        } else if (marks >= 65) {
            System.out.println("Grade c");
        } else {
            System.out.println("Fail");
        }

        System.out.println("Rest of the code.....");

    }

}
